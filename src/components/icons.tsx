export function Icon({ name, size, className, ...props }: IconProps) {
  const IconComponent = icons[name]

  if (!IconComponent) {
    if (process.env.NODE_ENV === 'development') {
      console.warn(`Icon "${name}" not found`)
    }
    return null
  }

  return (
    <IconComponent
      className={cn('inline-block', className)}
      width={size}
      height={size}
      {...props}
    />
  )
}

